<?php

namespace Hotel\ProjectBundle\Entity;

/**
 * ChhPaisCongreso
 */
class ChhPaisCongreso
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $nombrePais;

    public function __toString(){
        return $this->nombrePais.' ';
    }
    
    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nombrePais
     *
     * @param string $nombrePais
     *
     * @return ChhPaisCongreso
     */
    public function setNombrePais($nombrePais)
    {
        $this->nombrePais = $nombrePais;

        return $this;
    }

    /**
     * Get nombrePais
     *
     * @return string
     */
    public function getNombrePais()
    {
        return $this->nombrePais;
    }
}
