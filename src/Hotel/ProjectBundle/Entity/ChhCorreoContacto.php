<?php

namespace Hotel\ProjectBundle\Entity;

use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\ORM\Mapping as ORM;

/**
 * ChhCorreoContacto
 * @ORM\Entity()
 * @ORM\HasLifecycleCallbacks()
 */
class ChhCorreoContacto
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $areaContacto;

    /**
     * @var string
     */
    private $correoContacto;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set areaContacto
     *
     * @param string $areaContacto
     *
     * @return ChhCorreoContacto
     */
    public function setAreaContacto($areaContacto)
    {
        $this->areaContacto = $areaContacto;

        return $this;
    }

    /**
     * Get areaContacto
     *
     * @return string
     */
    public function getAreaContacto()
    {
        return $this->areaContacto;
    }

    /**
     * Set correoContacto
     *
     * @param string $correoContacto
     *
     * @return ChhCorreoContacto
     */
    public function setCorreoContacto($correoContacto)
    {
        $this->correoContacto = $correoContacto;

        return $this;
    }

    /**
     * Get correoContacto
     *
     * @return string
     */
    public function getCorreoContacto()
    {
        return $this->correoContacto;
    }
}
